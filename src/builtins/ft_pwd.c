void	pwd(char *envp[])
{
	int	i;

	i = 0;
	if (!envp)
		return (NULL);
	while (ft_strncmp(envp[i], "PWD=", 4) != 0 && envp[i])
		i++;
	ft_printf("%s\n", ft_strchr(envp[i], '='));
}
